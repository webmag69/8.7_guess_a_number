document.getElementById('btnLess').addEventListener('click', function () { // Обработчик кнопки "Меньше"
// документ. получитьЭлементПоИдентификатору ('бтнМеньше'). добавитьПрослушивательСобытий('click', функция() {
    if (gameRun){                                                    // Проверяем, что игра запущена (gameRun=играБеги)
        if (minValue === maxValue){                                  // Проверяем, равны ли минимальное и максимальное числа (т.е. индетично)
            const phraseRandom = Math.round( Math.random());         // Генерируем случайное число 0 или 1 (const фраза Случайный = Math. раунд(Математика случайный());)
            const answerPhrase = (phraseRandom === 1) ?              // Проверяем, равно ли случайное число 1 (const фраза ответа = (фразаСлучайный === 1) ?)
                `У нас такого числа нет!\n\u{1F9ED}` :               // Если да, то выводим первую фразу (смайлик компас)
                `У меня нет вариантов...\n\u{1F937}`;                // Если нет, то выводим вторую фразу (смайлик пожимающий плечами)   

            answerField.innerText = answerPhrase;                    // Выводим фразу в поле ответа (Поле ответа. внутренний текст = фраза ответа;)
            gameRun = false;                                         // Останавливаем игру (играЗапущена = ложь;)    
        } else {
            maxValue = answerNumber;                             // Увеличиваем новое минимальное число на 0 (максимальное значение = номер ответа + 0;)
            answerNumber = Math.floor((minValue + maxValue) / 2);    // Получаем новое предположение NB! Здесь происходит округление среднего арифметического "вниз" (вычисляем новое число)
            // номерОтвета = *Math.floor-Округление к меньшему целому* ((минимальноеЗначение + максимальноеЗначение) / 2);
            orderNumber++;                                           // Увеличиваем номер попытки (номер заказа++;)
            orderNumberField.innerText = orderNumber;                // Выводим номер попытки (Поле номера заказа. внутренний текст = номер заказа;)
            answerField.innerText = `Это число ${answerNumber }?`;   // Выводим предположение (Поле ответа. inner Text = `Вы загадали число ${answer Number }?`;)
        }
    }
})



