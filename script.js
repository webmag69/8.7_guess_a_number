alert(`Загадайте любое целое число от ${minValue} до ${maxValue}, а я его угадаю`); // Предлагаем пользователю загадать число
let answerNumber  = Math.floor((minValue + maxValue) / 2);            // Получаем первое предположение (answer=отвечать)
let orderNumber = 1;                                                  // Номер попытки (порядковый номер)
let gameRun = true;                                                   // Флаг для проверки, что игра запущена (начало игры)

const orderNumberField = document.getElementById('orderNumberField'); // Получаем элементы для вывода номера попытки и ответа
const answerField = document.getElementById('answerField');           // Получаем элементы для вывода номера попытки и ответа

orderNumberField.innerText = orderNumber;                             // Выводим номер попытки
answerField.innerText = `Вы загадали число ${answerNumber }?`;        // Выводим первое предположение

function text(answerNumber){                                          // функциональный текст (номер ответа) {
    var ones = ['', 'один', 'два', 'три', 'четыре', 'пять', 'шесть', 'семь', 'восемь', 'девять',
                'десять', 'одиннадцать', 'двенадцать', 'тринадцать', 'четырнадцать', 'пятнадцать', 'шестнадцать',
                'семнадцать', 'восемнадцать', 'девятнадцать'];
    var tens = ['', '', 'двадцать', 'тридцать', 'сорок', 'пятьдесят', 'шестьдесят', 'семьдесят', 'восемьдесят',
                'девяносто'];
    var hundreds = ['', 'сто', 'двести', 'триста', 'четыреста', 'пятьсот', 'шестьсот', 'семьсот', 'восемьсот', 'девятьсот'];
    var numString = answerNumber.toString();                          // var num Строка = номер ответа. нанизывать();
    if (answerNumber < 20) {                                          // если (номер_ответа < 20) {
      return ones[answerNumber];                                      // вернуть единицы [номер_ответа];
    }
    if (numString.length === 2) {                                     // если (число строк. длина === 2) {
      return tens[numString[0]] + ' ' + ones[numString[1]];           // вернуть десятки [число строк [0]] + ' ' + единицы [число строк [1]];
    }
    if (numString.length === 3) {                                     // если (число строк. длина === 3) {
      if (numString[0] === '0' && numString[2] === '1')               // if (число строк[0] === '0' && число строк[2] === '1')
        return hundreds[numString[0]] + ones[numString[1]];
      else
        return hundreds[numString[0]] + ' ' + tens[numString[1]] + ' ' + ones[numString[2]];
    }
  }
  text(answerNumber);



