document.getElementById('btnEqual').addEventListener('click', function () { // Обработчик кнопки "Верно!" (кнопка Верно)
    if (gameRun){                                                           // Проверяем, что игра запущена
        let phraseRandom = Math.round(Math.random() * 3);
        switch (phraseRandom) {                                             // переключатель (фраза Случайный)
            case 0:                                                         // кейс
                answerPhrase = `Ещё разок?\n\u{1F9D0}`;                     // ответФраза = смайлик с моноклем
                break;
            case 1:
                answerPhrase = `Скучно!\n\u{1F971}`;                        // ответФраза = зивающий смайлик
                break;
            case 2:
                answerPhrase = `И всё?\n\u{1F92D}`;                         // ответФраза = смеющийся смайлик
                break;
            case 3:
                answerPhrase = `Повторим?\n\u{1F917}`;                      // ответФраза = довольный смайлик
                break;
        }
        answerField.innerText = answerPhrase;                               // Выводим фразу
        gameRun = false;                                                    // Останавливаем игру
    }
})



